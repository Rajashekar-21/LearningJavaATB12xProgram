package ex_010_For_Loop;

public class Lab108_ForLoop_P3 {
    public static void main(String[] args) {
        for (int i = -1; i > -10; i--)
        {
            System.out.println(i);
        }
        int i = 1;
        for (;i<10;i++)
        {
            System.out.println(i);
        }
    }
}
