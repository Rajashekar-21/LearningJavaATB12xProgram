package ex_010_For_Loop;

public class Lab107_ForLoop_2 {
    public static void main(String[] args) {
        for (int i = 0; i < 10 ; i++)
        {
            System.out.println(i);
        }
        for (float f = 0; f < 10.67; f++)
        {
            System.out.println(f);
        }
    }
}
