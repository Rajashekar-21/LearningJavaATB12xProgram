package ex_09_Switch;

public class Lab096_Interview {
    public static void main(String[] args) {
        char code = 'C';
        switch (code)
        {
            case 'A'  :
                System.out.println("hi");
                break;
            case 'B':
                System.out.println("hello");
                break;
            default:
                System.out.println("Hi i am default");
        }


    }
}
